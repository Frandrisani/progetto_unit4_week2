package CatalogoBibliografico.entities;

import CatalogoBibliografico.interfaces.Catalogo;

import java.time.LocalDate;

public class Book implements Catalogo {
    // Attributes
    private long isbn;
    private String titolo;
    private LocalDate annoPubblicazione;
    private int numeroPagine;
    private String autore;
    private String genere;

    // Constructor
    public Book(long isbn, String titolo, LocalDate annoPubblicazione, int numeroPagine, String autore, String genere) {
        this.isbn = isbn;
        this.titolo = titolo;
        this.annoPubblicazione = annoPubblicazione;
        this.numeroPagine = numeroPagine;
        this.autore = autore;
        this.genere = genere;
    }

    public long getISBN() {
        return isbn;
    }

    public String getTitolo() {
        return titolo;
    }

    public LocalDate getAnnoPubblicazione() {
        return annoPubblicazione;
    }

    public int getNumeroPagine() {
        return numeroPagine;
    }

    public String getAutore() {
        return autore;
    }

    public String getGenere() {
        return genere;
    }

    @Override
    public String toString() {
        return "Book{" +
                "isbn=" + isbn +
                ", titolo='" + titolo + '\'' +
                ", annoPubblicazione=" + annoPubblicazione +
                ", numeroPagine=" + numeroPagine +
                ", autore='" + autore + '\'' +
                ", genere='" + genere + '\'' +
                '}';
    }
}
